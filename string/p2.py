'''Changing Case of a String:
We can change case of a string by using the following 4 methods.'''
print(__doc__)

s = input('Enter String:')

print('Entered String in Upper case: ',s.upper())
print('Entered String in Lower case: ',s.lower())
print('Entered String in Title form: ',s.title())
print('Entered String in Swap case: ',s.swapcase())
print('Entered String in Capitalize form: ',s.capitalize())
